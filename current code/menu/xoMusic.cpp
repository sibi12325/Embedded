// Non-blocking music playback variables
unsigned long xoPreviousMillis = 0;
int xoCurrentNote = 0;
int xoMelodySize = sizeof(xoDurations) / sizeof(int);
int xoNoteDuration = 0;
int xoPauseBetweenNotes = 0;

void playXOmusic() {
  unsigned long currentMillis = millis();

  // Check if enough time has passed to move to the next note
  if (currentMillis - xoPreviousMillis >= xoNoteDuration + xoPauseBetweenNotes) {
    // If we're still in the xoMelody
    if (xoCurrentNote < xoMelodySize) {
      // Calculate the note's duration
      xoNoteDuration = 1000 / xoDurations[xoCurrentNote];
      tone(BUZZER_PIN, xoMelody[xoCurrentNote], xoNoteDuration * 0.9); // Play note

      // Update the time when this note was started
      xoPreviousMillis = currentMillis;

      // Set the pause between notes (30% of note duration)
      xoPauseBetweenNotes = xoNoteDuration * 0.30;

      // Move to the next note
      xoCurrentNote++;
    } else {
      // If the xoMelody is finished, stop the tone and reset
      noTone(BUZZER_PIN);
      xoCurrentNote = 0; // Optionally restart the xoMelody
    }
  }
}
